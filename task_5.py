# Ввести ціле число і вивести його у
# 2 - 8 - 16 - 10 системах числення

number = int(input("Введіть ціле число: "))
print("десяткова система числення: ", number)
print("Двійкова система числення: ", bin(number))
print("Вісімкова система числення: ", oct(number))
print("Шістнадцяткова система числення: ", hex(number))
